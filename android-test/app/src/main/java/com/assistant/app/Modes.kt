package com.assistant.app

/**
 * Моды (отдельные чаты с собственным системным промптом и памятью).
 * Название / цвет / системный промпт — всё в одном месте.
 */
object Modes {
    data class Mode(
        val id: String,
        val name: String,
        val color: Int,
        val systemPrompt: String
    )

    val all: List<Mode> = listOf(
        Mode(
            id = "nutrition",
            name = "Питание",
            color = 0xFF4CAF50.toInt(),
            systemPrompt = """Ты — ассистент по питанию. Помогай считать калории, БЖУ, воду. Когда пользователь сообщает о еде, отвечай кратко: примерная калорийность, белки/жиры/углеводы, оценка за день. Используй формат:
КАЛОРИИ: ~X ккал
БЕЛКИ: ~X г
ЖИРЫ: ~X г
УГЛЕВОДЫ: ~X г
ВОДА: ~X л
Если данных не хватает — задай 1-2 уточняющих вопроса. Кодируй итог дня в конце сообщения отдельной строкой `ИТОГ: 1800 ккал / 90Б / 70Ж / 200У / 1.5л` если речь про подсчёт за день."""
        )
    )

    fun byId(id: String): Mode? = all.firstOrNull { it.id == id }
}
